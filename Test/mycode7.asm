.MODEL SMALL
.STACK
.DATA
    CHUOI1 DW 13,'MOI BAN NHAP MOT KI TU HOA: $'
    CHUOI2 DW 10,13,'KI TU THUONG LA: $'  
.CODE
    MOV AX, @DATA
    MOV DS,AX
    
    BATDAU:
    MOV DX, OFFSET CHUOI1
    MOV AH,9
    INT 21H
    
    MOV AH,1
    INT 21H
    
    KIEMTRA1:
    CMP AL,'A'
    JNB KIEMTRA2
    JMP BATDAU
    
    KIEMTRA2:
    CMP AL,'Z'
    JNA HOPLE
    JMP BATDAU
    
    HOPLE:
    MOV CL,AL
    MOV DX, OFFSET CHUOI2
    MOV AH,9
    INT 21H
    
    MOV DL,CL
    ADD DL,20H
    MOV AH,2
    INT 21H
    
    THOAT:
    MOV AH,4CH
    INT 21H
END